// Apply translations to the page
function applyTranslations(language) {
    const trans = translations[language] || translations.en;
    
    // Update all elements with data-translate-key attribute
    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
        const translateType = element.getAttribute('data-translate-type');
        
        if (trans[key]) {
            if (translateType === 'placeholder') {
                element.placeholder = trans[key];
            } else {
                element.textContent = trans[key];
            }
        }
    });
    
    // Update filter options
    updateFilterOptions(language);
    
    // Re-render companies to update sector and location names
    if (typeof applyFilters === 'function') {
        applyFilters();
    }
}

function updateFilterOptions(language) {
    const trans = translations[language] || translations.en;
    
    // Update sector filter options
    const sectorFilter = document.getElementById('sectorFilter');
    Array.from(sectorFilter.options).forEach(option => {
        if (option.value !== 'all' && trans[option.value]) {
            option.textContent = trans[option.value];
        }
    });
    
    // Update location filter options
    const locationFilter = document.getElementById('locationFilter');
    Array.from(locationFilter.options).forEach(option => {
        if (option.value !== 'all' && trans[option.value]) {
            option.textContent = trans[option.value];
        }
    });
}

// Initialize translations
document.addEventListener('DOMContentLoaded', function() {
    // Apply default language
    applyTranslations('en');
}); 